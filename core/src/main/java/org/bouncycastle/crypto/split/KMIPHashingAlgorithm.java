package org.bouncycastle.crypto.split;

/**
 * The HashingAlgorithm enum represents various hashing algorithms
 * used in cryptographic operations.
 */
public class KMIPHashingAlgorithm
{
    public static final int MD2 = 0x01;           // MD2 hashing algorithm
    public static final int MD4 = 0x02;           // MD4 hashing algorithm
    public static final int MD5 = 0x03;           // MD5 hashing algorithm
    public static final int SHA_1 = 0x04;         // SHA-1 hashing algorithm
    public static final int SHA_224 = 0x05;       // SHA-224 hashing algorithm
    public static final int SHA_256 = 0x06;       // SHA-256 hashing algorithm
    public static final int SHA_384 = 0x07;       // SHA-384 hashing algorithm
    public static final int SHA_512 = 0x08;       // SHA-512 hashing algorithm
    public static final int RIPEMD_160 = 0x09;    // RIPEMD-160 hashing algorithm
    public static final int TIGER = 0x0A;         // Tiger hashing algorithm
    public static final int WHIRLPOOL = 0x0B;     // Whirlpool hashing algorithm
    public static final int SHA_512_224 = 0x0C;   // SHA-512/224 hashing algorithm
    public static final int SHA_512_256 = 0x0D;   // SHA-512/256 hashing algorithm
    public static final int SHA3_224 = 0x0E;      // SHA3-224 hashing algorithm
    public static final int SHA3_256 = 0x0F;      // SHA3-256 hashing algorithm
    public static final int SHA3_384 = 0x10;      // SHA3-384 hashing algorithm
    public static final int SHA3_512 = 0x11;      // SHA3-512 hashing algorithm
    //EXTENSIONS("8XXXXXXX");    // Extensions for future use
}

